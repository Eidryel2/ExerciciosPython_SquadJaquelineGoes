"""
n de horas de exercicio fisico por semana. queima-se 5 calorias por minuto.

calcule as calorias queimadas no mês. 
"""

horas_de_exercicio = int(input("Entre com o total de horas de exercício físico na semana: "))

calorias_queimadas = (horas_de_exercicio * 300) * 4

print("O total de calorias queimadas no mês é: ", calorias_queimadas)

